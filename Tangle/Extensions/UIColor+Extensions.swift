import Foundation

extension UIColor {
    class func tangleBlueColor() -> UIColor {
        return UIColor(red:0.23, green:0.52, blue:0.73, alpha:1.00)
    }
    
    class func offWhiteColor() -> UIColor {
        return UIColor(red:0.96, green:0.96, blue:0.96, alpha:1.00)
    }
}
